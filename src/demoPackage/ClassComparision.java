package demoPackage;

public class ClassComparision 
{
	public static void main(String[] args) 
	{
		// Comparision
		//greater than
		int a=20;
		int b=10;
		boolean result=a>b;
		System.out.println(result);
		
		//less than
		result=b<a;
		System.out.println(result);
		
		//equal to(==)
		result=(a==b);
		System.out.println(result);
		
		//greater than
		result=(a>=b);
		System.out.println(result);
		
		//less than equal to(<=)
		result=(a<=b);
		System.out.println(result);
		
		// not equal to(!=)
		result=(a!=b);
		System.out.println(result);
		
		
	}

}
