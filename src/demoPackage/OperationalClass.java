package demoPackage;

public class OperationalClass 
{
	public static void main(String[] args)
	{
		int a=12;
		int b=5;
		
		boolean result=a>b;
		System.out.println(result);
		
		//less than 
		result=a<b;
		System.out.println(result);
		
		//greater than or equal to
		
		result=a>=b;
		System.out.println(result);
		
		//less than or equal to
		
		result=a<=b;
		System.out.println(result);
		
		//equal to ==
		
		result=a==b;
		System.out.println(result);
		
		//not equal to !=
		result=a!=b;
		System.out.println(result);
		
		
		
	}

}
