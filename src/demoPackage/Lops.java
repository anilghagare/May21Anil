package demoPackage;

public class Lops {
	public static void main(String[] args)
	{
		//for loop
		// for(variable initialization;condition;increment/decrement)
		
		
		
		int i ;
		for (i=0;i<5;i++)
		{System.out.println("Hello");}
		 
		for(int j=0;j<5;j++) 
		{System.out.println(j);}
		
		for(int r=5;r>0;r--) 
		{System.out.println(r);}
		
		for(int b=10;b>=1;b--) 
		{System.out.println(b);}
		
		//*****
		
		for(int s=0;s<5;s++)
		{System.out.println('*');}
		
	}

}
