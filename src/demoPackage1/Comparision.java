package demoPackage1;

public class Comparision {
	public static void main(String[] args) {
		int a=12;
		int b=13;
		boolean c =a>b;
		System.out.println(c);
		c=a<b;
		System.out.println(c);
		c=(a<=b);
		System.out.println(c);
		c=(a>=b);
		System.out.println(c);
		c=a==b;
		System.out.println(c);
		c=a!=b;
		System.out.println(c);
		
	}

}
