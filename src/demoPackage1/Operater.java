package demoPackage1;

public class Operater {
	public static void main(String[] args) {
		int a=10;
		int b=5;
		int c=a+b;
		System.out.println(c);
		c=a/b;
		System.out.println(c);
		c=a%b;
		System.out.println(c);
		int i=10;
		i++;
		System.out.println(i);
		i--;
		System.out.println(i);
		float p1=12.5f;
		float p2=3.5f;
		float p3=p1/p2;
		System.out.println(p3);
		p3=p1%p2;
		System.out.println(p3);
		
		
		
		
	}

}
